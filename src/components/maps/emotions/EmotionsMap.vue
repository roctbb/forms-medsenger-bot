<script>
export default {
    name: "EmotionsMap",
    props: ['parts', 'width', 'uid'],
    data() {
        return {
            originalWidth: 812,
            originalHeight: 812
        }
    },
    computed: {
        size() {
            let w = this.mobile ? window.innerWidth * 0.8 : window.innerWidth * 0.5
            return `width: ${w}px; height: ${this.originalHeight * this.proportion}px;`
        },
        proportion() {
            let w = this.mobile ? window.innerWidth * 0.8 : window.innerWidth * 0.5
            return w / this.originalWidth
        }
    },
    methods: {
        mouse: function (action, zone) {
            Event.fire(`mouse-${action}`, {zone: zone, uid: this.uid})
        },
        color: function (zone, color) {
            return this.parts.filter(p => p == zone).length ? color : (color + '77')
        },
        stroke: function (zone) {
            return this.parts.filter(p => p == zone).length ? '4px' : '1px'
        }
    }
}
